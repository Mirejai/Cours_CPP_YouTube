

// fonction main
int main() {

    // Déclaration d'une variable de type int (nombre entier signé)
    int variable1;

    // Assignation d'une valeur à variable1
    variable1 = 314; // À partir de là variable1 contiendra 314

    // Assignation d'une autre valeur à variable1
    variable1 = 1'234; // À partir de là variable1 contiendra 1 234

    // Déclaration d'un variable de type int avec initialisation
    int variable_initialisé{-8};

    // Déclaration d'une variable avec initialisation par copie
    int variable_copiée{variable_initialisé};

    // Variable de type double (nombre à virgule)
    double variable_2{8.0674};

    // Variable de type bool (vrai ou faux)
    bool suis_je_le_meilleur{true};

    // Variable de type char (contient un caractère)
    char lettre_g{'G'};

    return 0; // Instruction obligatoire à la fin de main()
}

